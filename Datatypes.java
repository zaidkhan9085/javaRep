 import java.io.*;
 import java.util.*;
 class DataTypes
{
 
  
   public static void main(String[] args)
 {
  //byte

byte a = 10;

System.out.println("value of byte data type is :"+a); 

//short

short b = -234;

System.out.println("value of short data type is :"+b); 

//Integer

int c = 20000;

System.out.println("value of Integer data type is :"+c); 

//long

long d = 100000;

System.out.println("value of long data type is :"+d); 

//float

float e = 10.2f;

System.out.println("value of float data type is :"+e); 

//double

double f = 11.23;

System.out.println("value of double data type is :"+f); 
  }
   }
   
  